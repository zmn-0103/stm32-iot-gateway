/* 辅助函数                                                            */
/* ------------------------------------------------------------------ */

static uint32_t OLED_Pow(uint32_t X, uint32_t Y)
{
    uint32_t Result = 1;
    while (Y--) Result *= X;
    return Result;
}

/**
 * @brief  在缓冲区中写入一个字符 (8x16)
 * @param  Line   行 1~4
 * @param  Column 列 1~16
 * @param  Char   ASCII 字符
 * @param  invert 0=正常, 1=反色
 */
static void OLED_WriteChar(uint8_t Line, uint8_t Column, char Char, uint8_t invert)
{
    if (Line < 1 || Line > 4 || Column < 1 || Column > 16)
        return;
    if (Char < ' ' || Char > '~')
        return;

    uint8_t idx = (uint8_t)(Char - ' ');
    uint8_t page_upper = (Line - 1) * 2;      /* 上半部分页地址 */
    uint8_t page_lower = (Line - 1) * 2 + 1;  /* 下半部分页地址 */
    uint8_t col = (Column - 1) * 8;            /* 起始列地址 */

    for (uint8_t i = 0; i < 8; i++) {
        uint8_t upper = OLED_F8x16[idx][i];
        uint8_t lower = OLED_F8x16[idx][i + 8];
        buffer[page_upper * 128 + col + i] = invert ? ~upper : upper;
        buffer[page_lower * 128 + col + i] = invert ? ~lower : lower;
    }
}


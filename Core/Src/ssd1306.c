/* ------------------------------------------------------------------ */
/* I2C 底层通信                                                        */
/* ------------------------------------------------------------------ */

static void SSD1306_WriteCmd(uint8_t cmd)
{
    uint8_t data[2] = {0x00, cmd};
    HAL_I2C_Master_Transmit(h_i2c, SSD1306_I2C_ADDR << 1, data, 2, 10);
}

static void SSD1306_WriteData(const uint8_t *data, uint16_t len)
{
    HAL_I2C_Master_Transmit(h_i2c, SSD1306_I2C_ADDR << 1,
                            (uint8_t *)data, len, 100);
}

/* ------------------------------------------------------------------ */
/* 初始化                                                              */
/* ------------------------------------------------------------------ */

void OLED_Init(I2C_HandleTypeDef *hi2c)
{
    h_i2c = hi2c;
    HAL_Delay(100);

    SSD1306_WriteCmd(0xAE);  /* Display OFF */
    SSD1306_WriteCmd(0xD5);  /* Set display clock */
    SSD1306_WriteCmd(0x80);
    SSD1306_WriteCmd(0xA8);  /* Set multiplex ratio */
    SSD1306_WriteCmd(0x3F);  /* 64-1 */
    SSD1306_WriteCmd(0xD3);  /* Set display offset */
    SSD1306_WriteCmd(0x00);
    SSD1306_WriteCmd(0x40);  /* Set start line = 0 */
    SSD1306_WriteCmd(0xA1);  /* Segment re-map */
    SSD1306_WriteCmd(0xC8);  /* COM scan direction remapped */
    SSD1306_WriteCmd(0xDA);  /* COM pins config */
    SSD1306_WriteCmd(0x12);
    SSD1306_WriteCmd(0x81);  /* Set contrast */
    SSD1306_WriteCmd(0xCF);
    SSD1306_WriteCmd(0xD9);  /* Set pre-charge period */
    SSD1306_WriteCmd(0xF1);
    SSD1306_WriteCmd(0xDB);  /* Set VCOMH */
    SSD1306_WriteCmd(0x30);
    SSD1306_WriteCmd(0x8D);  /* Charge pump */
    SSD1306_WriteCmd(0x14);
    SSD1306_WriteCmd(0xA4);  /* Entire display ON (follow RAM) */
    SSD1306_WriteCmd(0xA6);  /* Normal display */
    SSD1306_WriteCmd(0xAF);  /* Display ON */

    OLED_Clear();
    OLED_Update();
}


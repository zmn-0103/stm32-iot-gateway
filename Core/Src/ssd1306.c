{
    SSD1306_WriteCmd(0x21);  /* Column address */
    SSD1306_WriteCmd(0x00);
    SSD1306_WriteCmd(0x7F);
    SSD1306_WriteCmd(0x22);  /* Page address */
    SSD1306_WriteCmd(0x00);
    SSD1306_WriteCmd(0x07);

    /* 分 4 块发送 (每块 256 字节) */
    for (int i = 0; i < 4; i++) {
        SSD1306_WriteData(&buffer[i * 256], 256);
    }
}


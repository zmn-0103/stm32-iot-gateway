void OLED_ShowChar(uint8_t Line, uint8_t Column, char Char)
{
    OLED_WriteChar(Line, Column, Char, 0);
}

void OLED_ShowString(uint8_t Line, uint8_t Column, char *String)
{
    while (*String) {
        if (Column > 16) break;
        OLED_WriteChar(Line, Column, *String, 0);
        Column++;
        String++;
    }
}

void OLED_ShowNum(uint8_t Line, uint8_t Column, uint32_t Number, uint8_t Length)
{
    for (uint8_t i = 0; i < Length; i++) {
        OLED_WriteChar(Line, Column + i,
                       Number / OLED_Pow(10, Length - i - 1) % 10 + '0', 0);
    }
}

void OLED_ShowSignedNum(uint8_t Line, uint8_t Column, int32_t Number, uint8_t Length)
{
    uint32_t abs_num;
    if (Number >= 0) {
        OLED_WriteChar(Line, Column, '+', 0);
        abs_num = Number;
    } else {
        OLED_WriteChar(Line, Column, '-', 0);
        abs_num = -Number;
    }
    for (uint8_t i = 0; i < Length; i++) {
        OLED_WriteChar(Line, Column + 1 + i,
                       abs_num / OLED_Pow(10, Length - i - 1) % 10 + '0', 0);
    }
}

void OLED_ShowFloat(uint8_t Line, uint8_t Column, float value)
{
    char buf[12];
    snprintf(buf, sizeof(buf), "%.1f", (double)value);
    OLED_ShowString(Line, Column, buf);
}
